#include "stress_init.h"

//======================================标准窗口============================================
bool init() {
  // 初始化ncurses，也可以使用newterm()函数
  initscr();
  // 更新屏幕
  flush();
}

void shutdown() {
  // 退出ncurses
  endwin();
}

